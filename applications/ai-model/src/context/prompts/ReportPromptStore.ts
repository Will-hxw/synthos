            "- 选择 5-8 个最重要主题做全景式深度回顾。",
            "- 每个主题都要覆盖：起因与背景、阶段演进、多方观点、结论与决策、影响评估、未解决问题。",
            "",
            "## 📈 五、趋势与模式识别",
            "- 识别本月反复出现的问题模式、机会信号、协作摩擦或系统性变化。",
