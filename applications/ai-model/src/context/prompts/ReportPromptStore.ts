    /**
     * 获取不同报告类型的执行要求。
     * @param reportType 报告类型
     */
    private static _getReportSummaryRequirements(reportType: "half-daily" | "weekly" | "monthly"): string[] {
        const commonRequirements = [
            "语言流畅自然，表达清晰专业。可以像知识文章或 Newsletter 一样有标题感、节奏感和阅读温度，但不得影响信息准确性",
            "使用 Markdown 格式，必须包含 # 标题、> 核心观点、## 目录式章节、### 小节、✅ 要点、📌 结论、🔭 展望、📎 附录等固定骨架",
            "emoji 使用要丰富但克制：每个一级标题（##）必须且只能搭配 1 个贴切 emoji；正文可少量使用 ✅、📌、🔭 等功能性 emoji，但不要满屏堆叠",
            "先写总论和核心观点，再分主题展开，最后给出趋势、启示、行动建议和展望；整体必须像文章，不像日志或数据库导出",
            "优先按主题聚合话题，不要机械逐条平铺；完整话题清单放在附录中，正文重点分析主题、趋势、冲突和行动含义",
            "严禁编造输入材料中没有的事实、数字、责任人、链接、截止时间、结论或决策；材料不足时必须写「未明确」「待确认」或省略具体值",
            "如果引用了某个话题的内容，请在句子末尾标注来源，格式类似：[话题7] [话题11] [话题23]",
            "标注中的数字 N 为 1-based，并严格对应上方「话题列表」的序号（第 N 条话题即 [话题N]）",
            "每个话题的来源标注应在其相关段落的末尾集中出现，方便读者回溯原文",
            "杜绝空洞套话，例如「引起广泛关注」「大家进行了热烈讨论」「具有重要意义」；必须说明具体事实、观点、影响或后续动作",
            "请直接输出综述文本，不要添加任何前缀或后缀说明（如「以下是XX报告：」之类）",
            "根据话题数量和信息密度动态控制篇幅：话题少时不要硬凑长文，话题多时自然展开；不设置固定字数，但必须内容详实、结构完整"
        ];

        if (reportType === "half-daily") {
            return [
                "半日报偏事件追踪：重点讲清本时段发生了什么、关键转折是什么、哪些后续动作需要继续跟进",
                "正文优先按主题聚合相近话题，但附录必须覆盖「话题列表」中的每一条话题，确保没有话题完全消失",
                "每个被纳入正文分析的主题都要包含：背景与起因、讨论脉络与转折、核心观点与论据、结论与后续动作、影响与启示",
                "如果某个话题信息量较少，可以放入附录并简洁说明其核心内容；不要为了凑篇幅虚构上下文",
                "利用跨话题连接主动发现话题之间的内在联系——因果关系、共同指向的系统性问题、或值得关注的矛盾张力",
                "统计解读部分不要只复述数字，要解释数字背后的含义和原因",
                ...commonRequirements
            ];
        }

        if (reportType === "weekly") {
            return [
                "周报偏主题归纳：不要把七天内容拼成流水账，要把分散事件整理成少数高价值主题、趋势和行动建议",
                "话题分类要合理且有洞察力——不只是打标签，还要分析每类主题的核心议题、进展状态和内在张力",
                "重点主题的深度展开必须做到：读者读完后能够理解其来龙去脉和意义，即使读者没有参与原讨论",
                "趋势洞察部分要体现真正的分析思维——不只是说「这些话题有关联」，而要具体说明关联机制是什么（因果？共同根源？对立？）",
                "数据洞察部分要解释数字背后的含义——为什么本周活跃度是这样的？峰值出现在这个时间反映了什么？不同群的活跃度差异意味着什么？",
                "群组生态观察要结合具体话题数据——不要给空洞的评价，要用实际讨论内容来佐证你的观察",
                "总结与展望部分要有实质内容——具体的关注方向、明确的待解决问题、有依据的预判",
                ...commonRequirements
            ];
        }

        return [
            "严禁将月报写成「周报的加长版」。月报的核心价值在于宏观视角——站在更高的维度回顾、对比、洞察",
            "月度全景部分要用叙事性的语言描绘本月讨论的完整图景——像写一个月的「故事」，有起承转合",
            "数据深度解读如包含环比对比，必须来自输入材料或明确可用的历史信息；材料不足时不得编造对比数字",
            "主题分类与演变分析是月报的独特价值——本月哪些话题类型在升温？哪些在降温？反映了什么变化？",
            "重大主题全景回顾必须做到：每个主题都配有完整的因果链分析、多方观点梳理、阶段性转折标注、最终影响评估",
            "趋势与模式识别要体现宏观判断力——哪些是偶发事件？哪些是系统性变化的信号？",
            "战略洞察与建议要有高度——不要给琐碎的操作建议，要提出对团队或项目有方向性指导意义的大局判断",
            "下月展望要有依据——基于当前趋势和未完结话题做出合理预判，不要空泛猜测",
            ...commonRequirements
        ];
    }
